class Digit:
    def __init__(self, neopixel):
        self.np: NeoPixel = neopixel
        self.lines = []
        self.screen = []
        self.pixelfield = []

    def _generate_lines(self) -> list:
        for _ in range(16):
            line = []
            for _ in range(16):
                line.append(self.pixelfield.pop(0))
            self.lines.append(line)

    def _line_is_odd(self, line_number: int) -> bool:
        if line_number % 2 == 1:
            return True
        else:
            return False
    
    def _reverse_all_odd_lines(self):
        for line_number, line in enumerate(self.lines):
            if self._line_is_odd(line_number):
                line.reverse()

    def _append_all_lines_to_screen(self) -> None:
        for line in self.lines:
            for _ in range(16):
                self.screen.append(line.pop())

    def show_on_screen(self, pixelfield) -> None:
        self.lines = []
        self.screen = []
        self.pixelfield = pixelfield.copy()
        self._generate_lines()
        self._reverse_all_odd_lines()
        self._append_all_lines_to_screen()
        self.np.fill([0, 0, 0])
        for i, pixel in enumerate(self.screen):
            if pixel:
                self.np[i] = [10, 10, 10]
        self.np.write()

    def clear(self):
        self.np.fill([0, 0, 0])
        self.np.write()
        


def main():
    neopixel_0 = NeoPixel(machine.Pin(DIGIT_0_PIN), 256)
    neopixel_1 = NeoPixel(machine.Pin(DIGIT_1_PIN), 256)
    neopixel_2 = NeoPixel(machine.Pin(DIGIT_2_PIN), 256)
    digit_0 = Digit(neopixel_0)
    digit_1 = Digit(neopixel_1)
    digit_2 = Digit(neopixel_2)
    while True:
        for i in range(len(NUMBERS)):
            digit_0.show_on_screen(NUMBERS[i])
            digit_1.show_on_screen(NUMBERS[i])
            digit_2.show_on_screen(NUMBERS[i])
            time.sleep(0.1)
            digit_0.clear()
            digit_1.clear()
            digit_2.clear()


if __name__ == '__main__':
    main()